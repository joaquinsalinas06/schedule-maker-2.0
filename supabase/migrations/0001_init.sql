-- ============================================================================
-- 8. Row Level Security
-- ============================================================================

-- 8.1 Catalog tables: public read-only. Writes happen via service role
--     (migration scripts, admin tooling), which bypasses RLS entirely --
--     no insert/update/delete policies are defined here on purpose.
alter table public.universities             enable row level security;
alter table public.courses                  enable row level security;
alter table public.sections                 enable row level security;
alter table public.sessions                 enable row level security;
alter table public.curricula                enable row level security;
alter table public.curriculum_courses       enable row level security;
alter table public.curriculum_prerequisites enable row level security;

create policy "catalog read: universities" on public.universities
  for select to anon, authenticated using (true);
create policy "catalog read: courses" on public.courses
  for select to anon, authenticated using (true);
create policy "catalog read: sections" on public.sections
  for select to anon, authenticated using (true);
create policy "catalog read: sessions" on public.sessions
  for select to anon, authenticated using (true);
create policy "catalog read: curricula" on public.curricula
  for select to anon, authenticated using (true);
create policy "catalog read: curriculum_courses" on public.curriculum_courses
  for select to anon, authenticated using (true);
create policy "catalog read: curriculum_prerequisites" on public.curriculum_prerequisites
  for select to anon, authenticated using (true);

-- 8.2 profiles
alter table public.profiles enable row level security;

create policy "profiles: select for authenticated" on public.profiles
  for select to authenticated using (true);

create policy "profiles: update own row" on public.profiles
  for update to authenticated
  using (auth.uid() = id)
  with check (auth.uid() = id);

-- 8.3 schedules
alter table public.schedules enable row level security;

create policy "schedules: owner full access" on public.schedules
  for all to authenticated
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

create policy "schedules: public read" on public.schedules
  for select to anon, authenticated
  using (is_public = true);

create policy "schedules: friends read" on public.schedules
  for select to authenticated
  using (
    exists (
      select 1 from public.friend_requests fr
      where fr.status = 'accepted'
        and (
          (fr.sender_id = auth.uid() and fr.receiver_id = schedules.user_id)
          or
          (fr.receiver_id = auth.uid() and fr.sender_id = schedules.user_id)
        )
    )
  );

-- 8.4 friend_requests
alter table public.friend_requests enable row level security;

create policy "friend_requests: select participants" on public.friend_requests
  for select to authenticated
  using (auth.uid() in (sender_id, receiver_id));

create policy "friend_requests: insert as sender, not anonymous" on public.friend_requests
  for insert to authenticated
  with check (
    auth.uid() = sender_id
    and coalesce((auth.jwt() ->> 'is_anonymous')::boolean, false) = false
  );

create policy "friend_requests: update by receiver" on public.friend_requests
  for update to authenticated
  using (auth.uid() = receiver_id)
  with check (auth.uid() = receiver_id);

create policy "friend_requests: delete by participants" on public.friend_requests
  for delete to authenticated
  using (auth.uid() in (sender_id, receiver_id));

-- 8.5 user_curriculum_progress
alter table public.user_curriculum_progress enable row level security;

create policy "user_curriculum_progress: owner full access" on public.user_curriculum_progress
  for all to authenticated
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

