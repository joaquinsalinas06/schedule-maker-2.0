-- ============================================================================
-- schedule-maker-2 :: initial Supabase schema
-- Migrates the app off the FastAPI/SQLAlchemy + Railway Postgres backend.
--
-- Design notes:
--   * Catalog tables (universities, courses, sections, sessions, curricula,
--     curriculum_courses, curriculum_prerequisites) mirror backend/app/models/*.py
--     column-for-column so `pg_dump --data-only --column-inserts` from the old
--     Railway Postgres can be replayed directly against these tables.
--   * `users` (old FastAPI table) is replaced by Supabase `auth.users` +
--     `public.profiles`. Anything that referenced users.id (int) now
--     references auth.users(id) (uuid). Old integer user data cannot be
--     restored as-is into these uuid columns -- it must be remapped during
--     migration (out of scope for this file).
--   * `schedule_sessions` join table is intentionally dropped -- schedules now
--     store their session combination directly in `combination_data` jsonb.
--   * `friendships` table is intentionally dropped -- "are we friends" is
--     derived from an accepted row in `friend_requests` (either direction),
--     per the RLS policies below. There is no separate friendships table.
-- ============================================================================

-- ----------------------------------------------------------------------------
-- 0. Extensions
-- ----------------------------------------------------------------------------
create extension if not exists pgcrypto; -- gen_random_uuid()

-- ----------------------------------------------------------------------------
-- 1. Shared trigger function: keep updated_at current
-- ----------------------------------------------------------------------------
create or replace function public.set_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

